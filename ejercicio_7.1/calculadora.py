
class Calculator:
    
    def __init__(self, a, b):
        self.a = a
        self.b = b 
        
    def suma (self):
        return self.a + self.b 
    
    def resta(self):
        return self.a - self.b
    
    def divide (self):
        return self.a / self.b 
    
    def multiplication (self):
        return self.a * self.b 
    